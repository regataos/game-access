// Confirmation notification to cancel installation
function confirm_cancel_installation() {
	$(document).ready(function() {
		$(".confir-title").text("Realmente cancelar a instalação?");
		$(".true-button").text("Sim");
		$(".false-button").text("Não");
	});
}

